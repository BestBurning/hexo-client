<template>
  <section class="main">
    <Form ref="form" :model="post" :label-width="60" style="width: 100%;">
      <Row>
        <Col :span="12">
        <FormItem label="标题" prop="title">
          <Input v-model="post.title"></Input>
        </FormItem>
        </Col>

        <Col :span="12">
        <FormItem label="作者" prop="title">
          <Input v-model="post.author"></Input>
        </FormItem>
        </Col>
        </Col>
      </Row>

      <Row>
        <Col :span="24">
        <FormItem label="标签" prop="title">
          <Tag v-for="tag in post.tags" :key="tag" :name="tag" closable @on-close="delTag">{{ tag }}</Tag>
          <AutoComplete
              :data="tags"
              :filter-method="filterTags"
              v-model="inputTagText"
              @on-select="addTag"
              @keyup.native.enter="inputTag"
              size="small"
              placeholder="Input tag"
              style="width:120px">
          </AutoComplete>
        </FormItem>
        </Col>
      </Row>

      <Row>
        <Col :span="24" style="margin: 0px;">
        <editor ref="editor" :editor-height="editorHeight" :value="post.content" input-active-name="edit"
                v-model="post.content"></editor>
        </Col>
      </Row>

      <Row>
        <Col :span="24">
        <Button type="primary" icon="ios-compose" style="float: right; margin-right: 10px;" @click="writePost">发表
        </Button>
        </Col>
      </Row>
    </Form>
  </section>
</template>

<script>
  import Editor from '@/components/Editor.vue'

  export default {
    data () {
      return {
        editorHeight: '400px',
        tags: [],
        post: {
          title: '',
          content: '',
          author: '',
          tags: []
        },
        inputTagText: ''
      }
    },
    methods: {
      handleResize () {
        this.editorHeight = (document.documentElement.clientHeight - 280) + 'px'
      },
      writePost () {
        this.$store.dispatch('writePost', this.post)
      },

      /**
       * search tags
       */
      filterTags (value, option) {
        return option.toUpperCase().indexOf(value.toUpperCase()) !== -1
      },

      /**
       * 添加标签
       */
      addTag (tag) {
        var contains = false
        for (var i = 0; i < this.post.tags.length; i++) {
          if (this.post.tags[i].toUpperCase() === tag.toUpperCase()) {
            contains = true
            break
          }
        }
        if (contains) {
          this.$Message.warning('标签已存在。')
        } else {
          this.post.tags.push(tag)
        }
      },

      /**
       * input tag
       */
      inputTag () {
        this.addTag(this.inputTagText)
      },

      /**
       * 删除标签
       * @param event
       * @param name
       */
      delTag (event, name) {
        const index = this.post.tags.indexOf(name)
        this.post.tags.splice(index, 1)
      }
    },

    mounted () {
      this.handleResize()
      window.addEventListener('resize', this.handleResize)

      this.post.author = this.$store.state.Hexo.config.author
      this.tags = this.$store.state.Hexo.tags
    },

    beforeDestroy () {
      window.removeEventListener('resize', this.handleResize)
    },

    components: {
      Editor
    }
  }
</script>

